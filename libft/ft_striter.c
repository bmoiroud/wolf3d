#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;

	i = 0;
	if (s != NULL && (*f) != NULL)
	{
		while (*s)
		{
			(*f)(s);
			i++;
			s++;
		}
	}
}
