#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *src)
{
	char	*dst;
	int		i;

	i = 0;
	if ((dst = (char *)malloc((ft_strlen(src) + 1) * sizeof(char))) == NULL)
		return (NULL);
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
